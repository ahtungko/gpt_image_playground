export function inlineTranslate(locale: Locale | undefined, zhText: string, values: Values = {}) {
  const normalized = normalizeLocale(locale)
  const template = normalized === 'zh' ? zhText : en[zhText] ?? zhText
  return template.replace(/\{(\w+)\}/g, (_, name: string) => {
    const value = values[name]
    return value == null ? '' : String(value)
  })
}
