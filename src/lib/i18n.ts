export const messages: Record<Locale, Record<MessageKey, string>> = { en, zh }

export function normalizeLocale(value: unknown): Locale {
  return value === 'zh' ? 'zh' : 'en'
}

export function getDefaultLocale(): Locale {
  if (typeof navigator !== 'undefined') {
    const language = navigator.language?.toLowerCase() ?? ''
    if (language.startsWith('zh')) return 'zh'
  }
  return 'en'
}

export function translate(locale: Locale | undefined, key: MessageKey, values: Values = {}): string {
  const template = messages[normalizeLocale(locale)][key] ?? messages.en[key] ?? key
  return template.replace(/\{(\w+)\}/g, (_, name: string) => {
    const value = values[name]
    return value == null ? '' : String(value)
  })
}

export function getDateLocale(locale: Locale | undefined): string {
  return normalizeLocale(locale) === 'zh' ? 'zh-CN' : 'en-US'
}

